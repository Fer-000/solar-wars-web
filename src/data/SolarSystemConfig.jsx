// Generate deterministic angle from moon name (always the same for all players)
const hashMoonName = (name) => {
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = (hash << 5) - hash + name.charCodeAt(i);
    hash = hash & hash; // Convert to 32bit integer
  }
  // Normalize to 0-2π range
  return (Math.abs(hash) % 628) / 100; // 628/100 ≈ 2π
};

